// Operators are symbols that perform operations on variables and values. JavaScript provides several types of operators:

// 1. Arithmetic Operators
// Used to perform mathematical calculations.

// Operator	Description	Example (a = 10, b = 5)
// +	Addition	a + b → 15
// -	Subtraction	a - b → 5
// *	Multiplication	a * b → 50
// /	Division	a / b → 2
// %	Modulus (Remainder)	a % b → 0
// **	Exponentiation (Power)	a ** 2 → 100
// ++	Increment (+1)	a++ → 11
// --	Decrement (-1)	b-- → 4
// 2. Comparison Operators
// Used to compare two values and return true or false.

// Operator	Description	Example (a = 10, b = 5)
// ==	Equal to (loose comparison)	a == 10 → true
// ===	Strict equal (value & type)	a === "10" → false
// !=	Not equal to	a != b → true
// !==	Strict not equal (value & type)	a !== "10" → true
// >	Greater than	a > b → true
// <	Less than	a < b → false
// >=	Greater than or equal to	a >= 10 → true
// <=	Less than or equal to	b <= 5 → true
// 3. Logical Operators
// Used to combine multiple conditions.

// Operator	Description	Example (x = true, y = false)
// &&	Logical AND	x && y → false
// `		`
// !	Logical NOT	!x → false
// 4. Assignment Operators
// Used to assign values to variables.

// Operator	Description	Example (x = 10)	Equivalent To
// =	Assign	x = 10	x = 10
// +=	Add & Assign	x += 5 → 15	x = x + 5
// -=	Subtract & Assign	x -= 3 → 7	x = x - 3
// *=	Multiply & Assign	x *= 2 → 20	x = x * 2
// /=	Divide & Assign	x /= 2 → 5	x = x / 2
// %=	Modulus & Assign	x %= 3 → 1	x = x % 3
// **=	Power & Assign	x **= 2 → 100	x = x ** 2
// 5. Bitwise Operators
// Operate at the binary level.

// Operator	Description	Example (a = 5, b = 3)
// &	Bitwise AND	a & b → 1
// `	`	Bitwise OR
// ^	Bitwise XOR	a ^ b → 6
// ~	Bitwise NOT	~a → -6
// <<	Left Shift	a << 1 → 10
// >>	Right Shift	a >> 1 → 2